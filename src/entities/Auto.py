

class Auto:

    def __init__(self, modelo, anio, score):
        self._modelo = modelo
        self._anio = anio
        self._score = score


    @property
    def modelo(self):
        return self._modelo    
    
    @property
    def anio(self):
        return self._anio
    
    @property
    def modelo(self):
        return self._modelo
    

    @property
    def score(self):
        return self._score
    

